public class Variables {
    public static void main(String[] args) {
        //Example1
        String test="Name";
        test=test+"Surname";
        System.out.println(test);

        //Example2
        int i=5; //variable of primitive type
        System.out.println(i);
        double x=30.3;//variable of primitive type
        System.out.println(x);

        //Example3
        Integer a=new Integer(2);//variable of reference type
        System.out.println(a);
        String b=new String("Hello");//variable of reference type
        System.out.println(b);


    }
}
